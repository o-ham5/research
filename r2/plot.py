import networkx as nx
import matplotlib.pyplot as plt
import japanmap as jm

def plot_gc(G, nodecolor_dict, pos):
    color_map = []
    for v in G.nodes:
        color_map.append(nodecolor_dict[v])
    
    if pos:
        nx.draw_networkx(G, node_color=color_map, pos={n:n for n in G}, with_labels=False, node_size=500)
    else:
        nx.draw_networkx(G, node_color=color_map, with_labels=False, node_size=500)
    
    
    plt.axis('off')
    plt.show()

